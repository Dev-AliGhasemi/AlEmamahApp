package vira.alemamah.ModelHadisBottom;

public class ModelHadis {
    private String hadisArabi;
    private String hadisFarsi;
    private String narrator;
    private String address;

    public String getHadisArabi() {
        return hadisArabi;
    }

    public void setHadisArabi(String hadisArabi) {
        this.hadisArabi = hadisArabi;
    }

    public String getHadisFarsi() {
        return hadisFarsi;
    }

    public void setHadisFarsi(String hadisFarsi) {
        this.hadisFarsi = hadisFarsi;
    }

    public String getNarrator() {
        return narrator;
    }

    public void setNarrator(String narrator) {
        this.narrator = narrator;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
