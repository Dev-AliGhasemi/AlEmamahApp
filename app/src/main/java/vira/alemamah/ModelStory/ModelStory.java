package vira.alemamah.ModelStory;

public class ModelStory {
    String emam , name , text;
    boolean isSubTitle;

    public String getEmam() {
        return emam;
    }

    public void setEmam(String emam) {
        this.emam = emam;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public boolean isSubTitle() {
        return isSubTitle;
    }

    public void setSubTitle(boolean subTitle) {
        isSubTitle = subTitle;
    }
}
